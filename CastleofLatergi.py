import turtle   #writing latergi(use Farsi) with import turtle<<
def r_f(t,sx,sz):
    t.right(sx)
    t.forward(sz)
def l_f(t,sx,sz):
    t.left(sx)
    t.forward(sz)
def latargi():
    xo = turtle.Screen()
    xox = turtle.Turtle()
    xox.shape("turtle")
    xox.pensize(5)
    xox.penup()
    xox.forward(180)
    xox.pendown()
    r_f(xox,90,100)
    r_f(xox,90,50)
    xox.bk(25)
    r_f(xox,90,80)
    r_f(xox,180,80)
    xox.right(90)
    xox.penup()
    xox.fd(63)
    xox.pendown()
    r_f(xox,90,50)
    xox.penup()
    xox.forward(15)
    xox.pendown()
    xox.circle(5)
    xox.penup()
    r_f(xox,90,5)
    r_f(xox,90,5)
    r_f(xox,90,46)
    xox.right(180)
    xox.pendown()
    xox.circle(5)
    xox.penup()
    xox.forward(40)
    r_f(xox,90,60)
    xox.pendown()
    r_f(xox,90,60)
    r_f(xox,270,55)
    r_f(xox,90,45)
    xox.penup()
    r_f(xox,90,50)
    xox.right(270)
    xox.pendown()
    xox.forward(50)
    xox.penup()
    r_f(xox,90,15)
    xox.pendown()
    xox.forward(10)
    r_f(xox,90,50)
    r_f(xox,90,26)
    xox.penup()
    xox.forward(26)
    r_f(xox,90,26)
    xox.right(180)
    xox.pendown()
    xox.circle(5)
    xox.penup()
    l_f(xox,90,27)
    xox.left(90)
    xox.pendown()
    xox.forward(40)
    l_f(xox,90,50)
    r_f(xox,90,70)
    r_f(xox,90,38)
    r_f(xox,90,18)
    xo.mainloop()
latargi()